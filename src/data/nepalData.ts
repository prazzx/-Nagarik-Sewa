// Helper function to get districts sorted by province
export const getDistrictsByProvince = (provinceId?: number): District[] => {
  const sorted = [...districts].sort((a, b) => {
    // Sort by province first
    if (a.provinceId !== b.provinceId) {
      return a.provinceId - b.provinceId;
    }
    // Then by name
    return a.name.localeCompare(b.name);
  });
  
  if (provinceId) {
    return sorted.filter(d => d.provinceId === provinceId);
  }
  return sorted;
};

// Helper function to get province name
export const getProvinceName = (provinceId: number): string => {
  const province = provinces.find(p => p.id === provinceId);
  return province?.name || "";
};

// Helper function to get offices for a district and service
export const getOfficesForDistrict = (districtId: string, serviceId?: string): Office[] => {
  let filtered = offices.filter(o => o.districtId === districtId);
  if (serviceId) {
    filtered = filtered.filter(o => o.services.includes(serviceId));
  }
  return filtered;
};

// Helper to get DAO office or generate from district data
export const getDAOForDistrict = (districtId: string): Office | null => {
  const office = offices.find(o => o.districtId === districtId && o.officeType === "dao");
  if (office) return office;
  
  // Generate from district data if not explicitly listed
  const district = districts.find(d => d.id === districtId);
  if (district && district.daoPhone) {
    return {
      id: `dao-${districtId}`,
      name: `District Administration Office, ${district.name}`,
      nameNepali: `जिल्ला प्रशासन कार्यालय, ${district.nameNepali}`,
      districtId: districtId,
      provinceId: district.provinceId,
      address: `${district.headquarter}, ${district.name}`,
      phone: district.daoPhone,
      email: district.daoEmail || `dao.${districtId}@moha.gov.np`,
      hours: "10:00 AM - 5:00 PM (Sun-Fri)",
      services: ["citizenship", "passport", "marriage", "birth"],
      officeType: "dao",
    };
  }
  return null;
};
